package cn.computer.pojo;

public class accessories {
	private int id;//配件id
	private String cpu;//cpu
	private String graphics_card;//显卡
	private String hard_disk;//硬盘
	private String RAM;//运行内存
	private String resolution_ratio;//分辨率
	private String model;//型号
	private String color;//颜色
	private String screen_size;//屏幕尺寸
	private String os;//操作系统
	private String origin_of_goods;//配件场地
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCpu() {
		return cpu;
	}
	public void setCpu(String cpu) {
		this.cpu = cpu;
	}
	public String getGraphics_card() {
		return graphics_card;
	}
	public void setGraphics_card(String graphics_card) {
		this.graphics_card = graphics_card;
	}
	public String getHard_disk() {
		return hard_disk;
	}
	public void setHard_disk(String hard_disk) {
		this.hard_disk = hard_disk;
	}
	public String getRAM() {
		return RAM;
	}
	public void setRAM(String rAM) {
		RAM = rAM;
	}
	public String getResolution_ratio() {
		return resolution_ratio;
	}
	public void setResolution_ratio(String resolution_ratio) {
		this.resolution_ratio = resolution_ratio;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getScreen_size() {
		return screen_size;
	}
	public void setScreen_size(String screen_size) {
		this.screen_size = screen_size;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public String getOrigin_of_goods() {
		return origin_of_goods;
	}
	public void setOrigin_of_goods(String origin_of_goods) {
		this.origin_of_goods = origin_of_goods;
	}
}
